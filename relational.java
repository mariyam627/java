public class relational 
{
public static void main(String[] args) {
byte a=100;
byte b=50;
System.out.println(a+" < "+b+" "+(a<b));
System.out.println(a+" <= "+b+" "+(a<=b));
System.out.println(a+" > "+b+" "+(a>b));
System.out.println(a+" >= "+b+" "+(a>=b));
System.out.println(a+" == "+b+" "+(a==b));
System.out.println(a+" != "+b+" "+(a!=b));
}
}